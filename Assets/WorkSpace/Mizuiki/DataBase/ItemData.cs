using UnityEngine;

[System.Serializable]
[CreateAssetMenu(fileName = "ItemData", menuName = "CreateItem")]
public class ItemData : ScriptableObject
{
	[System.Serializable]
	public enum ItemType
	{
		STONE	= 0,	// 岩
		COAL,			// 石炭
		STEEL,			// 鉄
		TIN,			// 錫
		LEAD,			// 鉛

		BIRTHDAY_STONE = 200,	// ここから誕生石シリーズ
		BIR_GARNET,				// ガーネット
		BIR_AMETHYST,			// アメジスト
		BIR_AQUAMARINE,			// アクアマリン
		BIR_DIAMOND,			// ダイヤモンド
		BIR_EMERALD,			// エメラルド
		BIR_PEARL,				// パール
		BIR_RUBY,				// ルビー
		BIR_PERIDOT,			// ペリドット
		BIR_SAPPHIRE,			// サファイア
		BIR_OPAL,				// オパール
		BIR_TOPAZ,				// トパーズ
		BIR_TURQUOISE,			// ターコイズ

		OVER,
	}

	[Header("アイテム名")]
	public string itemName;
	[Header("アイテムの種類")]
	//public Type type;
	[CustomEnum(typeof(ItemType))] public string typeStr;
	[Header("アイテムの画像")]
	public Sprite sprite;
	[Header("アイテムのプレハブ")]
	public GameObject prefab = null;

	public ItemType type => SerializeUtil.Restore<ItemType>(typeStr);

	public ItemData(ItemData item)
	{
		itemName = item.itemName;
		typeStr = item.typeStr;
		sprite = item.sprite;
	}
}

[System.Serializable]
public class Items
{
	public ItemData.ItemType type;       // 種類
	public int count;       // 数
}

